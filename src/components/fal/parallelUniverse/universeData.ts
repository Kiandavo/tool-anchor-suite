// Helper functions for filtering universes
export const getUniversesByType = (type: string) => {
  if (type === 'all') return parallelUniverses;
  return parallelUniverses.filter(universe => universe.type === type);
};

export const searchUniverses = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return parallelUniverses.filter(universe => 
    universe.name.toLowerCase().includes(lowercaseQuery) ||
    universe.description.toLowerCase().includes(lowercaseQuery) ||
    universe.characteristics.some(char => char.toLowerCase().includes(lowercaseQuery))
  );
};
