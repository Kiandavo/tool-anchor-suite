// Helper function to create prediction arrays with the specified length
const createPredictionArray = (basePredictions: string[], targetCount: number = 200): string[] => {
  // If we already have enough predictions, return the array as is
  if (basePredictions.length >= targetCount) {
    return basePredictions;
  }
  
  // Otherwise, generate variations by adding wisdom phrases
  const wisdomPhrases = [
    "با صبر و شکیبایی می‌توان به اهداف بزرگ دست یافت.",
    "شناخت خود، اولین قدم در مسیر موفقیت است.",
    "هر سختی، فرصتی برای رشد و تعالی است.",
    "دنیای درون، انعکاسی از دنیای بیرون است.",
    "با مهربانی به دنیا بنگرید تا دنیا با شما مهربان باشد.",
    "هر پایانی، آغازی نو را به همراه دارد.",
    "آرامش درونی، بزرگترین گنج زندگی است.",
    "در سکوت و تأمل، حقیقت آشکار می‌شود.",
    "عشق، قدرتمندترین نیروی جهان هستی است.",
    "خرد حقیقی، در پذیرش ندانستن است.",
    "نور امید، تاریکی‌های زندگی را روشن می‌کند.",
    "شکوفایی درونی، به بیرون تابیده می‌شود."
  ];
  
  const expandedPredictions: string[] = [];
  
  // Use each base prediction as a seed for multiple variations
  basePredictions.forEach(prediction => {
    expandedPredictions.push(prediction);
    
    // Create variations with wisdom phrases
    wisdomPhrases.forEach(phrase => {
      expandedPredictions.push(`${prediction} ${phrase}`);
    });
    
    // Add some combined variations
    expandedPredictions.push(`${prediction} به یاد داشته باشید که ${wisdomPhrases[0].toLowerCase()}`);
    expandedPredictions.push(`${prediction} درک این نکته مهم است که ${wisdomPhrases[3].toLowerCase()}`);
  });
  
  // Ensure we don't exceed the target count
  return expandedPredictions.slice(0, targetCount);
};

