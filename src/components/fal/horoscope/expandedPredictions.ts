// Enhanced prediction generator with personalization
const createPersonalizedPredictions = (basePredictions: string[], targetCount: number = 300): string[] => {
  const expandedPredictions: string[] = [];
  
  basePredictions.forEach(prediction => {
    // Base prediction
    expandedPredictions.push(prediction);
    
    // Time-specific variations
    personalizedElements.timeSpecific.forEach(time => {
      expandedPredictions.push(`${prediction} این اتفاق ${time} محتمل‌تر است.`);
    });
    
    // Emotional state variations  
    personalizedElements.emotionalStates.forEach(emotion => {
      expandedPredictions.push(`${prediction} در این زمان ${emotion}.`);
    });
    
    // Number and color combinations
    const randomNumber = personalizedElements.specificNumbers[Math.floor(Math.random() * personalizedElements.specificNumbers.length)];
    const randomColor = personalizedElements.colors[Math.floor(Math.random() * personalizedElements.colors.length)];
    expandedPredictions.push(`${prediction} ${randomNumber} و ${randomColor} برای شما معنای خاصی خواهد داشت.`);
    
    // Relationship-specific predictions
    personalizedElements.relationships.forEach(relation => {
      expandedPredictions.push(`${prediction} این موضوع ${relation} مطرح خواهد شد.`);
    });
    
    // Wisdom combinations
    wisdomPhrases.forEach((phrase, index) => {
      if (index < 8) { // Limit to avoid too many combinations
        expandedPredictions.push(`${prediction} ${phrase}.`);
      }
    });
  });
  
  // Remove duplicates and shuffle
  const uniquePredictions = [...new Set(expandedPredictions)];
  
  // Shuffle array for randomness
  for (let i = uniquePredictions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [uniquePredictions[i], uniquePredictions[j]] = [uniquePredictions[j], uniquePredictions[i]];
  }
  
  return uniquePredictions.slice(0, targetCount);
};

