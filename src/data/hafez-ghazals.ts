export const getHafezPoemsCountByEmotion = (): Record<string, number> => {
  const counts: Record<string, number> = {};
  hafezGhazals.forEach(poem => {
    if (poem.emotion) {
      counts[poem.emotion] = (counts[poem.emotion] || 0) + 1;
    }
  });
  return counts;
};
