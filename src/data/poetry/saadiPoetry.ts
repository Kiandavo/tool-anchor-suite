// Get poem by theme
export const getSaadiPoemByTheme = (theme: SaadiPoem['theme']): SaadiPoem => {
  const themePoems = saadiPoems.filter(poem => poem.theme === theme);
  if (themePoems.length === 0) return getRandomSaadiPoem();
  const randomIndex = Math.floor(Math.random() * themePoems.length);
  return themePoems[randomIndex];
};

// Get poem by source
export const getSaadiPoemBySource = (source: SaadiPoem['source']): SaadiPoem => {
  const sourcePoems = saadiPoems.filter(poem => poem.source === source);
  if (sourcePoems.length === 0) return getRandomSaadiPoem();
  const randomIndex = Math.floor(Math.random() * sourcePoems.length);
  return sourcePoems[randomIndex];
};

